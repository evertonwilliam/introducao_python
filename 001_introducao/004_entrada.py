# Imprimindo um título para o exemplo
print("Recebendo entrada do usuário em Python")

# Recebendo entrada do usuário
nome = input("Digite seu nome: ")  # Recebe o nome do usuário
idade = input("Digite sua idade: ")  # Recebe a idade do usuário

# Exibindo a entrada do usuário
print("Nome:", nome)  # Exibe o nome do usuário
print("Idade:", idade)  # Exibe a idade do usuário

# Exemplo de formatação de string com a entrada do usuário
mensagem = f"Olá, {nome}! Você tem {idade} anos."  # Formata a string com os dados do usuário

# Exibindo a mensagem formatada
print(mensagem)  # Exibe a mensagem formatada
