print("Cálculos básicos em Python")

print("Soma: 2 + 3 = ", 2 + 3)  # Soma de dois números
print("Subtração: 5 - 2 = ", 5 - 2)  # Subtração de dois números
print("Multiplicação: 4 * 3 = ", 4 * 3)  # Multiplicação de dois números
print("Divisão: 10 / 2 = ", 10 / 2)  # Divisão de dois números
print("Módulo: 10 % 3 = ", 10 % 3)  # Módulo (resto da divisão)
print("Exponenciação: 2 ** 3", 2 ** 3)  # Exponenciação (2 elevado a 3)
print("Divisão inteira: 10 // 3", 10 // 3)  # Divisão inteira (sem o resto)
